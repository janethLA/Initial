let a =3, b = 2;

//Operador +
let z =a+b;
console.log("Resultado de la suma es: "+ z);

//Operador -
z = a-b;
console.log("Resultado de la resta es: "+ z);

//Operador *
z = a*b;
console.log("Resultado de la muly es: "+ z);

//Operador /
z = a/b;
console.log("Resultado de la div es: "+ z);

//Operador %
z = a % b;
console.log("Resultado de la mod es: "+ z);

//Operador **
z = a ** b;
console.log("Resultado de la exponente es: "+ z);

//INCREMENTO ++
//--> Pre-incremento (el operador  ++ antes de la variable)
z = ++a;
console.log(a); //a aumenta en 1
console.log(z); //despues se asigna el valor de a en z

//--> Post-incremento (el operador  ++ antes de la variable)
z = b++;
console.log(z); //se asigna el valor de b antes de incrementar, en z
console.log(b); //b aumenta en 1

//DECREMENTO --
//--> Pre-decremento (el operador  ++ antes de la variable)
z = --a;
console.log(a); //a disminue en 1
console.log(z); //despues se asigna el valor de a en z

//--> Post-decremento (el operador  ++ antes de la variable)
z = b--;
console.log(z); //se asigna el valor de b antes de disminuir, en z
console.log(b); //b disminue en 1




